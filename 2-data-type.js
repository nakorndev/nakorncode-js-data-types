// string = แสดงผลข้อความ ('foo', "bar")
// number = คำนวณคณิตศาสตร์ (11, -1, 1.25)
// boolean = ค่าจริงหรือเท็จ (true, false)

let myName = 'Nakorn'   // string
let myAge = 25          // number
let myWorkStatus = true // boolean

console.log('> myName:', myName, typeof myName)
console.log('> myAge:', myAge, typeof myAge)
console.log('> myWorkStatus:', myWorkStatus, typeof myWorkStatus)

